    <h1 class="text-3xl font-bold mb-4">Daśaśloki</h1>
    <p class="mb-4">The ten verses of the Daśaśloki are a collection of teachings attributed to Maharaj-ji, which encapsulate the essence of his philosophy and spiritual insights. Each verse serves as a guiding principle for seekers on their spiritual journey.</p>
    <ul class="list-disc pl-5 mb-4">
        <li class="mb-2">The first verse emphasizes the importance of self-realization and understanding one's true nature.</li>
        <li class="mb-2">The second verse highlights the significance of devotion and surrender to the divine.</li>
        <li class="mb-2">The third verse teaches the value of compassion and service to others.</li>
        <li class="mb-2">The fourth verse encourages seekers to cultivate inner peace and tranquility.</li>
        <li class="mb-2">The fifth verse reminds us of the impermanence of life and the need to focus on the eternal.</li>
        <li class="mb-2">The sixth verse speaks about the power of love and its transformative effects.</li>
        <li class="mb-2">The seventh verse emphasizes the importance of humility and letting go of ego.</li>
        <li class="mb-2">The eighth verse teaches the value of gratitude and appreciation for all that life offers.</li>
        <li class="mb-2">The ninth verse encourages seekers to remain steadfast in their spiritual practice.</li>
        <li>The tenth verse culminates in the realization that all paths ultimately lead to the same truth.</li>
    </ul>
