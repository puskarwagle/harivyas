        <h1 class="text-3xl font-bold mb-4 text-center">महावाणी · Mahāvāṇī</h1>

        <div class="prose prose-lg max-w-none mb-6">
            <p>
                <strong>Mahāvāṇī</strong> is the most revered collection of devotional bhajans in the <strong>Nimbarka Sampradaya</strong>. 
                These sacred utterances—composed by divine saints and realized acharyas—are considered direct expressions of 
                <em>prema-bhakti</em>, the highest form of pure, selfless love toward the Divine Couple, Shri Radha-Krishna.
            </p>

            <p>
                More than just poetic hymns, these vanīs are treated as <strong>spiritual revelations</strong>. Every syllable is infused with rasa (divine mood) 
                and siddhānta (spiritual truth), making the Mahāvāṇī not only a text for singing but also a means for <strong>transcendental realization</strong>.
            </p>

            <p>
                In our āśram, Mahāvāṇī is recited daily during <strong>mangal āratī</strong>, <strong>bhajan sandhyā</strong>, and special festival celebrations. 
                It is regarded as a form of <em>nāma-sankīrtana</em>, a spiritual practice that purifies the heart and connects the soul directly with the eternal līlā of the Lord.
            </p>

            <p>
                Many bhajans in Mahāvāṇī were composed by <strong>Shri Harivyasa Devacharya</strong> and other acharyas of the lineage. 
                They are preserved in <em>Braj Bhāṣā</em> and Sanskrit, and often memorized and meditated upon by sādhakas as part of their sādhanā.
            </p>
        </div>

